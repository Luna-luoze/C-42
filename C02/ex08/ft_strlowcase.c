char	*ft_strlowcase(char *str)
{
	char	*ori;

	ori = str;
	while (*str)
	{
		if (*str >= 'A' && *str <= 'Z')
			*str = *str + ('a' - 'A');
		str++;
	}
	return (ori);
}
