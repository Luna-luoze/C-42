char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	char			*ori;
	unsigned int	i;

	ori = dest;
	i = 0;
	while (*dest)
		dest++;
	while (*src && i < nb)
	{
		*dest = *src;
		dest++;
		src++;
		i++;
	}
	*dest = '\0';
	return (ori);
}
