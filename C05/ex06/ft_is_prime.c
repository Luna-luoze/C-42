int	ft_is_prime(int nb)
{
	int	fac;
	int	nb2;

	if (nb <= 1)
		return (0);
	fac = 2;
	nb2 = nb / 2;
	while (fac <= nb2)
	{
		if (nb % fac == 0)
			return (0);
		fac++;
	}
	return (1);
}
